const uint8_t g_num_presets = 36;

/* Load a preset into the working pattern */
void preset_load(uint8_t index)
{
    uint8_t t, s;
    const Pattern *p;

    if (index >= g_num_presets) index = 0;

    p = &g_presets[index];

    /* Copy steps - preserve velocity values (0=off, 1=soft, 2=med, 3=loud) */
    for (t = 0; t < NUM_TRACKS; t++)
        for (s = 0; s < NUM_STEPS; s++)
            g_pattern.steps[t][s] = p->steps[t][s];

    /* Copy metadata */
    for (t = 0; t < 16; t++)
        g_pattern.name[t] = p->name[t];

    g_pattern.kit   = p->kit;
    g_pattern.tempo = p->tempo;

    /* Apply kit and tempo */
    g_kit   = p->kit;
    seq_set_tempo((uint16_t)p->tempo);
}

void preset_get_name(uint8_t index, uint8_t *buf)
{
    uint8_t i;
    if (index >= g_num_presets) {
        for (i = 0; i < 16; i++) buf[i] = ' ';
        return;
    }
    for (i = 0; i < 16; i++)
        buf[i] = g_presets[index].name[i];
}